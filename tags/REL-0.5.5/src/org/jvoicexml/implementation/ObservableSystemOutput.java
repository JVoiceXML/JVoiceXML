package org.jvoicexml.implementation;


/**
 * A {@link org.jvoicexml.SystemOutput} that can be monitored by
 * {@link SystemOutputListener}s.
 *
 * <p>
 * Implementations must implement this interface to propagate input events
 * to the interpreter.
 * </p>
 *
 * @author Dirk Schnelle
 * @version $Revision$
 *
 * <p>
 * Copyright &copy; 2007 JVoiceXML group - <a
 * href="http://jvoicexml.sourceforge.net"> http://jvoicexml.sourceforge.net/
 * </a>
 * </p>
 *
 * @since 0.5.5
 */
public interface ObservableSystemOutput {
    /**
     * Sets the listener for system output events.
     *
     * <p>
     * The implementation of this interface must notify the listener
     * about all events.
     * </p>
     *
     * @param listener The listener.
     * @since 0.5
     */
    void setSystemOutputListener(final SystemOutputListener listener);
}
